# 자기소개 출력
# 구분선은 15개 문자 (=== 자기소개 === 길이 맞춤)
print("=== 자기소개 ===")
print(f"이름: {name}")
print(f"나이: {age}세")
print(f"직업: {job}")
print(f"취미: {hobby}")
